package com.smk.common.model;

import java.io.Serializable;
import lombok.Data;

/**
 * Table: COMMON_MSG_TEMPLATE
 */
@Data
public class CommonMsgTemplate implements Serializable {
    /**
     * 模板编号
     *
     * Column:    MSG_ID
     */
    private String msgId;

    /**
     * 模板类型 00 短信，01 邮件 02 站内信
     *
     * Column:    MSG_TYPE
     */
    private String msgType;

    /**
     * 标题
     *
     * Column:    MSG_TITLE
     */
    private String msgTitle;

    /**
     * 状态 0启用，1失效
     *
     * Column:    STATUS
     */
    private String status;

    /**
     * 消息内容
     *
     * Column:    MSG_CONTENT
     */
    private String msgContent;

    private static final long serialVersionUID = 1L;
}